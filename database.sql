-- 构建数据库以及测试数据

DROP DATABASE IF EXISTS attendance_system;
-- 创建数据库
CREATE DATABASE IF NOT EXISTS attendance_system;
USE attendance_system;
SET NAMES utf8mb4;
SET default_storage_engine=InnoDB;

SET FOREIGN_KEY_CHECKS = 0;

-- 1.用户表
CREATE TABLE IF NOT EXISTS user (
    user_id VARCHAR(20) PRIMARY KEY, -- 学号或工号
    password CHAR(60) NOT NULL, -- 密码
    user_type ENUM('student', 'teacher', 'admin') NOT NULL -- 用户身份
);

-- 2.学生表
CREATE TABLE IF NOT EXISTS student (
    student_id VARCHAR(20) PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    gender ENUM('男', '女') NOT NULL,
    department VARCHAR(50) NOT NULL,
    major VARCHAR(50) NOT NULL,
    grade VARCHAR(10) NOT NULL,
    birthday DATE,
    FOREIGN KEY (student_id) REFERENCES user(user_id),
    INDEX idx_student_dept (department), -- 院系索引
    INDEX idx_student_grade (grade) -- 年级索引
);

-- 3.教师表
CREATE TABLE IF NOT EXISTS teacher (
    teacher_id VARCHAR(20) PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    gender ENUM('男', '女') NOT NULL,
    department VARCHAR(50) NOT NULL,
    title VARCHAR(50) NOT NULL,
    birthday DATE NOT NULL,
    FOREIGN KEY (teacher_id) REFERENCES user(user_id)
);

-- 4.课程表
CREATE TABLE IF NOT EXISTS course (
    course_id VARCHAR(20) PRIMARY KEY,
    course_name VARCHAR(50) NOT NULL,
    credit INT NOT NULL DEFAULT 0,
    teacher_id VARCHAR(20) NOT NULL,
    department VARCHAR(50) NOT NULL,
    course_time VARCHAR(50) NOT NULL,
    course_location VARCHAR(50) NOT NULL,
    FOREIGN KEY (teacher_id) REFERENCES teacher(teacher_id)
);

-- 5.学生选课表
CREATE TABLE IF NOT EXISTS student_course (
    student_id VARCHAR(20) NOT NULL,
    course_id VARCHAR(20) NOT NULL,
    PRIMARY KEY (student_id, course_id),
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (course_id) REFERENCES course(course_id),
    INDEX idx_sc_course (course_id) -- 课程索引
);

-- 6.考勤记录表
CREATE TABLE IF NOT EXISTS attendance_log (
    log_id INT AUTO_INCREMENT PRIMARY KEY,
    student_id VARCHAR(20) NOT NULL,
    course_id VARCHAR(20) NOT NULL,
    check_time DATETIME NOT NULL,
    location BOOLEAN DEFAULT FALSE,
    check_state ENUM('checked', 'absent', 'leave') NOT NULL,
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (course_id) REFERENCES course(course_id),
    INDEX idx_log_student_course (student_id, course_id), -- 学生课程索引
    INDEX idx_log_composite (course_id, check_time, student_id) -- 复合索引
);

-- 7.签到要求表
CREATE TABLE IF NOT EXISTS attendance_requirement (
    course_id VARCHAR(20) NOT NULL,
    check_date DATE NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    location BOOLEAN DEFAULT NULL,
    PRIMARY KEY (course_id, check_date),
    FOREIGN KEY (course_id) REFERENCES course(course_id),
    INDEX idx_req_course (course_id, check_date) -- 复合索引
);

-- 8.考勤统计表
CREATE TABLE IF NOT EXISTS attendance_count (
    student_id VARCHAR(20) NOT NULL,
    course_id VARCHAR(20) NOT NULL,
    actual_count INT NOT NULL DEFAULT 0,
    need_count INT NOT NULL DEFAULT 0,
    PRIMARY KEY (student_id, course_id),
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (course_id) REFERENCES course(course_id)
);

SET FOREIGN_KEY_CHECKS = 1;


